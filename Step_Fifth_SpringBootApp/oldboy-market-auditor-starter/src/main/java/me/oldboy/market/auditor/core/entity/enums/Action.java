package me.oldboy.market.auditor.core.entity.enums;

/**
 * Перечисление возможных действий пользователя в системе.
 * Используется для логирования и контроля прав доступа.
 */
public enum Action {
    /**
     * Авторизация пользователя в системе
     */
    LOGIN,

    /**
     * Завершение сессии пользователя
     */
    LOGOUT,

    /**
     * Создание новой товарной позиции
     */
    ADD_PRODUCT,

    /**
     * Изменение данных существующего продукта
     */
    UPDATE_PRODUCT,

    /**
     * Удаление продукта из каталога
     */
    DELETE_PRODUCT
}
