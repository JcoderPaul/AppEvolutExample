package me.oldboy.market.config.utils;

/**
 * Интерфейс для доступа к конфигурационным параметрам системы.
 * Предоставляет абстракцию для чтения настроек из различных источников.
 */
public interface ConfigProvider {
    /**
     * Возвращает значение конфигурационного параметра по ключу.
     *
     * @param key ключ конфигурационного параметра
     * @return значение параметра или null если параметр не найден
     */
    String get(String key);
}
